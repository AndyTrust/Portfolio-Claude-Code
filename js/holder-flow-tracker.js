// Alias for render-intelligence.js compatibility
// Will be overwritten by tryLoadHolderMonitorJSON() if reports/holders_monitor_latest.json is available
let HOLDER_FLOW_TRACKER = Object.assign({}, HOLDER_FLOW_TRACKER_DYNAMIC);

// Try to load updated holder monitor from reports/holders_monitor_latest.json
// Works when served via HTTP; falls back to embedded data on file:// protocol
function tryLoadHolderMonitorJSON() {
  fetch('reports/holders_monitor_latest.json')
    .then(r => { if (!r.ok) throw new Error('not found'); return r.json(); })
    .then(data => {
      const tracker = data.tracker || data;
      if (tracker && Array.isArray(tracker.entries) && tracker.entries.length) {
        HOLDER_FLOW_TRACKER = tracker;
        // Re-render if Intelligence tab is visible
        const intPanel = document.getElementById('tab-intelligence');
        if (intPanel && intPanel.classList.contains('active')) renderHolderFlowMonitor();
      }
    })
    .catch(() => { /* Silently fall back to embedded HOLDER_FLOW_TRACKER_DYNAMIC */ });
}

// Run after DOM is ready
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', tryLoadHolderMonitorJSON);
} else {
  setTimeout(tryLoadHolderMonitorJSON, 300);
}
